/******************************************************************************
* \Syntax          : Dio_LevelType Switch_getState(Dio_ChannelType Switch_Id)        
* \Description     : Function that take the switch Id, read the level of switch channel then return it.                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : Switch_Id                     
* \Parameters (out): Level of DIO channel                                                      
* \Return value:   : Dio_LevelType  STD_LOW
                                    STD_HIGH
*******************************************************************************/
Dio_LevelType Switch_getState(Dio_ChannelType Switch_Id)
{
	if( Switch_Id == CHANNAL_F4 )
	{
		return Dio_ReadChannel(CHANNAL_F4);
	}
	else if( Switch_Id == CHANNAL_F0 )
	{
		return Dio_ReadChannel(CHANNAL_F0);
	}
		
}



